class Solution {
  public:
    int romanToDecimal(string &str) {
        // code here
        
        unordered_map<char,int> val;
        val['I'] = 1;
        val['V'] = 5;
        val['X'] = 10;
        val['L'] = 50;
        val['C'] = 100;
        val['D'] = 500;
        val['M'] = 1000;
        
        int res = 0;
        
        for(int i = str.length()-1; i >= 0;i--){
            
            if(i < str.length()-1 and val[str[i]] < val[str[i+1]]){
                
                res -= val[str[i]];
            }
            else{
                res += val[str[i]];
            }
        }
        return res;
    }
};

